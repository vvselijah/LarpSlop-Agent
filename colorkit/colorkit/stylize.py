"""colorkit.stylize — the STYLIZE stage (Stage 3) of the headless color engine.

This module owns the *creative look* layer that sits on top of an
already-neutralized (corrected + matched) image or shot. It deliberately does
NOT measure or correct pixels — that is the job of ``measure.py`` / ``correct.py``.
Here we only apply a pre-authored look, expressed either as a 3D LUT (``.cube``)
or as a lightweight split-tone.

Two parallel surfaces, mirroring the rest of the package:

* **Video path (deterministic, ffmpeg):** ``ffmpeg_lut_filter`` and
  ``ffmpeg_splittone_filter`` return filtergraph *strings*. The CLI
  (``color.py``) composes one constant correction filter + (optionally) one
  uniform look filter per shot, so the look is applied identically to every
  frame of the clip — no flicker, no per-frame divergence.
* **Still path (pixel, colour-science):** ``apply_look_image`` applies a
  ``.cube`` LUT to a single BGR array using tetrahedral interpolation, matching
  the ``lut3d=interp=tetrahedral`` ffmpeg apply so a still preview looks like
  the video grade.

LUT registry
------------
``LOOKS`` maps a friendly look name -> absolute path of its ``.cube`` file under
``colorkit/luts/``. The cube files themselves are generated by ``luts.py``
(stdlib-only); this module only references them by path and never imports
``luts.py`` (so a missing/ungenerated cube degrades to a clear error at apply
time, not an import-time failure).

Design rules honored:
* Tetrahedral interpolation everywhere (matches the ffmpeg apply and is the
  standard for film LUTs).
* BGR<->RGB and 0..255<->0..1 conversions are handled internally so callers
  pass and receive ordinary OpenCV BGR arrays.
* No network, no GPL deps, outputs never leave the engine here (this module
  returns strings / arrays; only ``color.py`` writes to ``out/``).
"""

